package com.afornalik.ox.view;

/**
 * This enum determine the manner in which the board will be displayed.
 * Value are use mainly in a UIExtended class as a parameter in a constructor.
 *
 * @author Andrzej Fornalik
 */

public enum DrawBoardType {

    /**
     * Representation for class which print board with number in a cell.
     */
    CELL,

    /**
     * Representation for class which print board with numbers on a axis.
     */
    SIDE
}
